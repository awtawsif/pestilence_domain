#define RESET "\033[0m"

void sleep_ms(int milliseconds)
{
#ifdef _WIN32
    Sleep(milliseconds);
#else
    usleep(milliseconds * 1000);
#endif
}
void clrscr()
{
    system("@cls||clear");
}

void print_formatted_message(char color[], char style[], char message[])
{
    if (style == NULL)
        style = "";

    sleep_ms(30);
    printf("%s%s%s%s", color, style, message, RESET);
}

void pnl()
{
    printf("\n");
}

